export type TranslationKey = keyof typeof T.fr;

@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly lang = signal<Lang>(
    (localStorage.getItem('bankos_lang') as Lang) ?? 'fr'
  );

  t(key: TranslationKey, vars?: Record<string, string>): string {
    let str = (T[this.lang()] as Record<string, string>)[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
      });
    }
    return str;
  }

  toggle(): void {
    const next: Lang = this.lang() === 'fr' ? 'en' : 'fr';
    this.lang.set(next);
    localStorage.setItem('bankos_lang', next);
  }
}
